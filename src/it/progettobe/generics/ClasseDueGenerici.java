package it.progettobe.generics;

public class ClasseDueGenerici<T, V> 
{
	private T campoT;
	private V campoV;
	
	public T getCampoT() {
		return campoT;
	}
	public void setCampoT(T campoT) {
		this.campoT = campoT;
	}
	public V getCampoV() {
		return campoV;
	}
	public void setCampoV(V campoV) {
		this.campoV = campoV;
	}
	
	
	
}
